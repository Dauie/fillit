#include "fillit.h"

void		ft_repchar(char *str, char o, char n, int max)
{
	int	i;

	i = 0;
	while (i <= max)
	{
		if ((i == 4 || i == 9 || i == 14 || i >= 19) && str[i] == o)
			str[i] = n;
		i++;
	}
	ft_trim_tet(str, '#');
}
