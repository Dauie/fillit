#include "fillit.h"

int		ft_validate_tets(char *str)
{
	int i;

	i = 0;
	while (v_tets[i])
	{
		if (ft_strequ(str, v_tets[i]) == 1)
			return (i);
		i++;
	}
	return (-1);
}
